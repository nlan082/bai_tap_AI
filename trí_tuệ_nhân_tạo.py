import numpy as np
import skfuzzy as fuzz
from skfuzzy import control as ctrl
#Tao ham lien thuoc inputs
food= ctrl.Antecedent(np.arange(0,10.1,0.1), 'food')
service=ctrl.Antecedent(np.arange(0,10.1,0.1), 'service')
#Tao ham lien thuoc cho output
tip=ctrl.Consequent(np.arange(10,25.1,0.1), 'tip')

food['poor']=fuzz.trimf(food.universe, [0,0,5])
food['average']=fuzz.trimf(food.universe, [0,5,10])
food['good']=fuzz.trimf(food.universe, [5,10,10])
service['poor']=fuzz.trimf(service.universe, [0,0,5])
service['average']=fuzz.trimf(service.universe, [0,5,10])
service['good']=fuzz.trimf(service.universe, [5,10,10])
tip['small']=fuzz.trimf(tip.universe, [10,13.75,17.5])
tip['average']=fuzz.trimf(tip.universe, [13.75,17.5,21.25])
tip['much']=fuzz.trimf(tip.universe, [17.5,21.25,25])

food.view()

"""food.universe: anh xa du lieu"""

service.view()

tip.view()

rule1=ctrl.Rule(food['poor']&service['poor'], tip['small'])
rule2=ctrl.Rule(food['average']&service['poor'], tip['small'])
rule3=ctrl.Rule(food['good']&service['poor'], tip['small'])
rule4=ctrl.Rule(food['poor']&service['average'], tip['small'])
rule5=ctrl.Rule(food['average']&service['average'], tip['average'])
rule6=ctrl.Rule(food['good']&service['average'], tip['much'])
rule7=ctrl.Rule(food['poor']&service['good'], tip['average'])
rule8=ctrl.Rule(food['average']&service['good'], tip['much'])
rule9=ctrl.Rule(food['good']&service['good'], tip['much'])

#Mo phong va dieu khien
tipping_ctrl=ctrl.ControlSystem([rule1, rule2, rule3, rule4, rule5, rule6, rule7, rule8, rule9])
tipping=ctrl.ControlSystemSimulation(tipping_ctrl)
#Danh gia va kiem tra lai
tipping.input['food']=0
tipping.input['service']=0
#Xac dinh gia tru dieu khien
tipping.compute()
print(tipping.output['tip'])
tip.view(sim=tipping)

