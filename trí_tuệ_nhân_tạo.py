import numpy as np
import skfuzzy as fuzz
from skfuzzy import control as ctrl
#Inputs
traffic_condition=ctrl.Antecedent(np.arange(0, 11, 1), 'traffic')
distance_to_customer=ctrl.Antecedent(np.arange(0, 16, 1), 'distance')
weather_condition=ctrl.Antecedent(np.arange(0, 3, 1), 'weather')
order_preparation_time=ctrl.Antecedent(np.arange(0, 16, 1), 'time')
driver_fatigue_level=ctrl.Antecedent(np.arange(0, 11, 1), 'fatigue')
#Outputs
estimated_delivery_time=ctrl.Consequent(np.arange(0, 26, 1), 'delivery_time')
incentive_bonus=ctrl.Consequent(np.arange(0, 21, 1), 'incentive')
driver_performance_rating=ctrl.Consequent(np.arange(0, 6, 0.1), 'rating')
#dulieu dau vao
traffic_condition['good']=fuzz.trimf(traffic_condition.universe, [0, 0, 5])
traffic_condition['normal']=fuzz.trimf(traffic_condition.universe, [0, 5, 10])
traffic_condition['bad']=fuzz.trimf(traffic_condition.universe, [5, 10, 10])
distance_to_customer['short']=fuzz.trimf(distance_to_customer.universe, [0, 0, 7])
distance_to_customer['normal']=fuzz.trimf(distance_to_customer.universe, [0, 8, 15])
distance_to_customer['large']=fuzz.trimf(distance_to_customer.universe, [7, 15, 15])
weather_condition['good']=fuzz.trimf(weather_condition.universe, [0, 0, 1])
weather_condition['normal']=fuzz.trimf(weather_condition.universe, [0, 1, 2])
weather_condition['bad']=fuzz.trimf(weather_condition.universe, [1, 2, 2])
order_preparation_time['fast']=fuzz.trimf(order_preparation_time.universe, [0, 0, 5])
order_preparation_time['normal']=fuzz.trimf(order_preparation_time.universe, [0, 5, 10])
order_preparation_time['slow']=fuzz.trimf(order_preparation_time.universe, [5, 10, 15])
driver_fatigue_level['good']=fuzz.trimf(driver_fatigue_level.universe, [0, 0, 5])
driver_fatigue_level['normal']=fuzz.trimf(driver_fatigue_level.universe, [0, 5, 10])
driver_fatigue_level['bad']=fuzz.trimf(driver_fatigue_level.universe, [5, 10, 10])
estimated_delivery_time['fast']=fuzz.trimf(estimated_delivery_time.universe, [0, 5, 10])
estimated_delivery_time['normal']=fuzz.trimf(estimated_delivery_time.universe, [5, 10, 15])
estimated_delivery_time['slow']=fuzz.trimf(estimated_delivery_time.universe, [10, 15, 25])
incentive_bonus['small']=fuzz.trimf(incentive_bonus.universe, [0, 5, 10])
incentive_bonus['normal']=fuzz.trimf(incentive_bonus.universe, [5, 10, 15])
incentive_bonus['large']=fuzz.trimf(incentive_bonus.universe, [10, 15, 20])
driver_performance_rating['good']=fuzz.trimf(driver_performance_rating.universe, [0, 0, 3])
driver_performance_rating['normal']=fuzz.trimf(driver_performance_rating.universe, [2, 3, 4])
driver_performance_rating['bad']=fuzz.trimf(driver_performance_rating.universe, [3, 5, 5])

traffic_condition.view()

distance_to_customer.view()

weather_condition.view()

order_preparation_time.view()

driver_fatigue_level.view()

estimated_delivery_time.view()

incentive_bonus.view()

driver_performance_rating.view()

#giao thong+ khoang cach
rule1=ctrl.Rule(traffic_condition['good']&distance_to_customer['short'], estimated_delivery_time['fast'])
rule2=ctrl.Rule(traffic_condition['normal']&distance_to_customer['normal'], estimated_delivery_time['normal'])
rule3=ctrl.Rule(traffic_condition['bad']&distance_to_customer['large'], estimated_delivery_time['slow'])
#thoi tiet va tien thuong khuyen khich
rule4=ctrl.Rule(weather_condition['good'], incentive_bonus['small'])
rule5=ctrl.Rule(weather_condition['normal'], incentive_bonus['normal'])
rule6=ctrl.Rule(weather_condition['bad'], incentive_bonus['large'])
#thoi gian cb don hang va giao hang
rule7=ctrl.Rule(order_preparation_time['fast']&traffic_condition['good'], estimated_delivery_time['fast'])
rule8=ctrl.Rule(order_preparation_time['normal']&traffic_condition['normal'], estimated_delivery_time['normal'])
rule9=ctrl.Rule(order_preparation_time['slow']&traffic_condition['bad'], estimated_delivery_time['slow'])
#do met moi cua tai xe va xep hang hieu xuat
rule10=ctrl.Rule(driver_fatigue_level['good'], driver_performance_rating['good'])
rule11=ctrl.Rule(driver_fatigue_level['normal'], driver_performance_rating['normal'])
rule12=ctrl.Rule(driver_fatigue_level['bad'], driver_performance_rating['bad'])
#luat ket hop nhieu yeu to
rule13=ctrl.Rule(distance_to_customer['large']&traffic_condition['bad'], estimated_delivery_time['slow'], incentive_bonus['large'])
rule14=ctrl.Rule(distance_to_customer['short']&traffic_condition['good'], estimated_delivery_time['fast'], incentive_bonus['small'])
rule15=ctrl.Rule(driver_fatigue_level['bad']&traffic_condition['bad'], driver_performance_rating['bad'], estimated_delivery_time['slow'])
#them luat

control_system=ctrl.ControlSystem([rule1, rule2, rule3, rule4, rule5, rule6, rule7, rule8, rule9, rule10, rule11, rule12, rule13, rule14, rule15])
fuzzy_system=ctrl.ControlSystemSimulation(control_system)
fuzzy_system.input['traffic']=7
fuzzy_system.input['distance']=9
fuzzy_system.input['weather']=2
fuzzy_system.input['time']=10
fuzzy_system.input['fatigue']=8
fuzzy_system.compute()
print("Thời gian dự kiến:", fuzzy_system.output['delivery_time'])
print("Thưởng:", fuzzy_system.output['incentive'])
print("Đánh giá:", fuzzy_system.output['rating'])
