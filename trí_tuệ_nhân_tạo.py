import numpy as np
import skfuzzy as fuzz
from skfuzzy import control as ctrl
temperature=ctrl.Antecedent(np.arange(0, 41, 1), 'temperature')
humidity=ctrl.Antecedent(np.arange(0, 101, 1), 'humidity')
fan_speed=ctrl.Consequent(np.arange(0, 101, 1), 'fan_speed')#do phan giai thap to do xu li nhanh(dpg:1)
temperature['cold']=fuzz.trimf(temperature.universe, [0, 0, 20])
temperature['warm']=fuzz.trimf(temperature.universe, [15, 25, 40])
temperature['hot']=fuzz.trimf(temperature.universe, [30, 40, 40])
humidity['dry']=fuzz.trimf(humidity.universe, [0, 0, 50])
humidity['normal']=fuzz.trimf(humidity.universe, [30, 50, 70])
humidity['wet']=fuzz.trimf(humidity.universe, [50, 100, 100])
fan_speed['low']=fuzz.trimf(fan_speed.universe, [0, 0, 50])
fan_speed['medium']=fuzz.trimf(fan_speed.universe, [30, 50, 70])
fan_speed['high']=fuzz.trimf(fan_speed.universe, [50, 100, 100])

temperature.view()

humidity.view()

fan_speed.view()

rule1=ctrl.Rule(temperature['cold']&humidity['dry'], fan_speed['low'])
rule2=ctrl.Rule(temperature['cold']&humidity['normal'], fan_speed['low'])
rule3=ctrl.Rule(temperature['cold']&humidity['wet'], fan_speed['medium'])
rule4=ctrl.Rule(temperature['warm']&humidity['dry'], fan_speed['low'])
rule5=ctrl.Rule(temperature['warm']&humidity['normal'], fan_speed['medium'])
rule6=ctrl.Rule(temperature['warm']&humidity['wet'], fan_speed['high'])
rule7=ctrl.Rule(temperature['hot']&humidity['dry'], fan_speed['medium'])
rule8=ctrl.Rule(temperature['hot']&humidity['normal'], fan_speed['high'])
rule9=ctrl.Rule(temperature['hot']&humidity['wet'], fan_speed['high'])

fan_speed_ctrl=ctrl.ControlSystem([rule1, rule2, rule3, rule4, rule5, rule6, rule7, rule8, rule9])
fan_speed_simulation=ctrl.ControlSystemSimulation(fan_speed_ctrl)
fan_speed_simulation.input['temperature']=15
fan_speed_simulation.input['humidity']=40
fan_speed_simulation.compute()
print(f"Fan Speed: {fan_speed_simulation.output['fan_speed']}")
temperature.view(sim=fan_speed_simulation)
humidity.view(sim=fan_speed_simulation)
fan_speed.view(sim=fan_speed_simulation)

